
public class Player {
	int points = 0;
	int games = 0;
	int tiePoints = 0;
	String name = "";
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setPoints(int points) {
		this.points = points;
	}
	
	public void setTiePoints(int tiePoints) {
		this.tiePoints = tiePoints;
	}
	
	public void setGames(int games) {
		this.games = games;
	}

}
